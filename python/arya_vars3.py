# Variables can change, like Arya’s kills
kills = 2
print("Arya’s kills start at", kills)

# Add a kill, like crossing a name
kills = kills + 1
print("After one more, kills are", kills)

# Change it again, a new count
kills = 5
print("Now, Arya’s kills leap to", kills)